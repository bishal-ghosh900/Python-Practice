# For loop

for i in "Bishal":
    print(i, end = " ")
print("\n###########################")
for i in [1, 2, 3, "Bishal", "Ghosh"]:
    print(i)
print("###########################")
for i in range(10):
    print(i, end=" ") # 0 to till 9 incrementing by 1 in each iteration 
print("\n###########################")
for i in range(1, 10, 2):
    print(i, end=" ") # 1 to till 9 incrementing by 2 in each iteration 
    