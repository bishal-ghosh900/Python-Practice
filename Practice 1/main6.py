# Arithmatic Operator 

n1 = 10
n2 = 3

print(n1 / n2)  # 3.3333333333333335 --> It is the float division
print(n1 // n2) # 3 --> It is the integer division
print(n1 ** n2) # 1000 --> It the to the power sign

print("*" * 5) # *****
print("*" * 4) # ****
print("*" * 3) # ***
print("*" * 2) # **
print("*" * 1) # *


